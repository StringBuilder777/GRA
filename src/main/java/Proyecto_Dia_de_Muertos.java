    private void reproducirMusica(String rutaArchivo) {
        try {
            ProcessBuilder pb = new ProcessBuilder("mpv","--no-video", rutaArchivo);
            pb.inheritIO();
            pb.start();
        } catch (Exception e) {
            e.printStackTrace();
            System.out.println("Error al reproducir la msica: " + e.getMessage());
        }
    }


