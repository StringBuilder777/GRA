    public void animarFlamas(Canvas c) {
        // --- Zoom (ya lo tienes) ---
        ScaleTransition zoom = new ScaleTransition();
        zoom.setFromY(1);
        zoom.setToY(0.8);
        zoom.setFromX(1);
        zoom.setCycleCount(ScaleTransition.INDEFINITE);
        zoom.setAutoReverse(true);
        zoom.setDuration(Duration.seconds(3));
        zoom.setNode(c);

        // --- Desvanecer (simula apagarse la flama) ---
        FadeTransition desvanecer = new FadeTransition();
        desvanecer.setFromValue(1.0);
        desvanecer.setToValue(0.3);
        desvanecer.setCycleCount(FadeTransition.INDEFINITE);
        desvanecer.setAutoReverse(true);
        desvanecer.setDuration(Duration.seconds(3));
        desvanecer.setNode(c);

        // --- Mover hacia abajo (simula que la flama se apaga) ---
        TranslateTransition mover = new TranslateTransition();
        mover.setDuration(Duration.seconds(3));
        mover.setByY(10); // mueve 150 pxeles a la derecha
        mover.setCycleCount(TranslateTransition.INDEFINITE);
        mover.setAutoReverse(true);
        mover.setNode(c);

        // --- Ejecutar ambas animaciones juntas ---
        ParallelTransition animacion = new ParallelTransition(zoom, desvanecer, mover);
        animacion.play();
    }

